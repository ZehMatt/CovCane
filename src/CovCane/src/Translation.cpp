static asmjit::Operand convertOperandRegister(
    const ZydisDecodedInstruction& instr, const ZydisDecodedOperand& op)
{
    return convertRegister(instr, op.reg.value);
}

static asmjit::Operand convertOperandMemory(
    const ZydisDecodedInstruction& instr, const ZydisDecodedOperand& op)
{
    auto& mem = op.mem;

    asmjit::x86::Mem res;
    res.setSize(op.size / 8);

    switch (mem.segment)
    {
        case ZYDIS_REGISTER_ES:
            res.setSegment(asmjit::x86::es);
            break;
        case ZYDIS_REGISTER_CS:
            res.setSegment(asmjit::x86::cs);
            break;
        case ZYDIS_REGISTER_FS:
            res.setSegment(asmjit::x86::fs);
            break;
        case ZYDIS_REGISTER_GS:
            res.setSegment(asmjit::x86::gs);
            break;
        case ZYDIS_REGISTER_SS:
            if (instr.attributes & ZYDIS_ATTRIB_HAS_SEGMENT_SS)
                res.setSegment(asmjit::x86::ss);
            break;
        case ZYDIS_REGISTER_DS:
            if (instr.attributes & ZYDIS_ATTRIB_HAS_SEGMENT_DS)
                res.setSegment(asmjit::x86::ds);
            break;
    }

    if (mem.base != ZYDIS_REGISTER_NONE)
    {
        res.setBase(convertRegister(instr, mem.base));
    }

    if (mem.index != ZYDIS_REGISTER_NONE)
    {
        res.setIndex(convertRegister(instr, mem.index));
    }

    uint32_t shift = 0;
    if (mem.scale > 0)
    {
        shift = asmjit::Support::ctz(mem.scale);
        res.setShift(shift);
    }

    if ((mem.base == ZYDIS_REGISTER_RIP || mem.base == ZYDIS_REGISTER_EIP)
        && mem.index == ZYDIS_REGISTER_NONE && shift == 0
        && mem.disp.hasDisplacement)
    {
        // If base is RIP we compute the absolute address.
        uintptr_t val;
        if (ZydisCalcAbsoluteAddress(&instr, &op, &val) == ZYDIS_STATUS_SUCCESS)
        {
            res.setBase(asmjit::x86::Reg());
            res.setOffset(val);
            res.setRel();
        }
        else
        {
            res.setOffset(mem.disp.value);
        }
    }
    else
    {
        if (mem.disp.hasDisplacement)
        {
            res.setOffset(mem.disp.value);
        }
    }

    return res;
}

static asmjit::Operand convertOperandImmediate(
    const ZydisDecodedInstruction& instr, const ZydisDecodedOperand& op)
{
    auto& imm = op.imm;

    asmjit::Imm res;

    if (imm.isRelative)
    {
        uintptr_t addr = 0;
        if (ZydisCalcAbsoluteAddress(&instr, &op, &addr)
            == ZYDIS_STATUS_SUCCESS)
        {
            res.setUPtr(addr);
        }
        else
        {
            // Bug?
            __debugbreak();
        }
    }
    else
    {
        // NOTE: Is this actually correct?.
        if (imm.isSigned)
            res.setI64(imm.value.s);
        else
            res.setU64(imm.value.u);
    }

    return res;
}

static asmjit::Operand convertOperand(
    const ZydisDecodedInstruction& instr, const ZydisDecodedOperand& op)
{
    switch (op.type)
    {
        case ZYDIS_OPERAND_TYPE_REGISTER:
            return convertOperandRegister(instr, op);
        case ZYDIS_OPERAND_TYPE_MEMORY:
            return convertOperandMemory(instr, op);
        case ZYDIS_OPERAND_TYPE_IMMEDIATE:
            return convertOperandImmediate(instr, op);
    }
    return asmjit::Operand();
}

bool convertInstruction(
    const ZydisDecodedInstruction& instr, asmjit::x86::Assembler& cb)
{
    if (instr.attributes & ZYDIS_ATTRIB_HAS_LOCK)
        cb.lock();

    if (instr.attributes & ZYDIS_ATTRIB_HAS_REP)
        cb.rep();

    if (instr.attributes & ZYDIS_ATTRIB_HAS_REPE)
        cb.repe();

    if (instr.attributes & ZYDIS_ATTRIB_HAS_REPNE)
        cb.repne();

    if (instr.attributes & ZYDIS_ATTRIB_HAS_REPNZ)
        cb.repnz();

    asmjit::Operand ops[5];

    int32_t usedOps = 0;
    for (int32_t i = 0; i < instr.operandCount; i++)
    {
        const auto& op = instr.operands[i];
        if (op.visibility == ZYDIS_OPERAND_VISIBILITY_HIDDEN)
        {
            continue;
        }
        ops[usedOps] = convertOperand(instr, op);
        usedOps++;
    }

    switch (instr.mnemonic)
    {
        case ZYDIS_MNEMONIC_NOP: {
            if (instr.length > 1)
            {
                // Workaround for unsupported long nop encodings.
                cb.embed(instr.data, instr.length);
            }
            else
            {
                // Single nop.
                cb.nop();
            }
            break;
        }
        case ZYDIS_MNEMONIC_CALL: {
            if (ops[0].isMem())
            {
                asmjit::x86::Mem& mem = ops[0].as<asmjit::x86::Mem>();
                if (mem.baseReg() == asmjit::x86::rsp && mem.hasOffset())
                {
                    cb.push(asmjit::x86::rax);
                    cb.mov(asmjit::x86::rax, instr.instrAddress + instr.length);
                    cb.xchg(
                        asmjit::x86::ptr(asmjit::x86::rsp), asmjit::x86::rax);
                    mem.addOffset(8);
                    cb.jmp(mem);
                }
                else
                {
                    cb.push(asmjit::x86::rax);
                    cb.mov(asmjit::x86::rax, instr.instrAddress + instr.length);
                    cb.xchg(
                        asmjit::x86::ptr(asmjit::x86::rsp), asmjit::x86::rax);
                    cb.jmp(mem);
                }
            }
            else if (ops[0].isImm())
            {
                cb.push(asmjit::x86::rax);
                cb.mov(asmjit::x86::rax, instr.instrAddress + instr.length);
                cb.xchg(asmjit::x86::ptr(asmjit::x86::rsp), asmjit::x86::rax);
                cb.emit(asmjit::x86::Inst::kIdJmp, ops[0]);
            }
            else
            {
                cb.emit(asmjit::x86::Inst::kIdCall, ops[0]);
            }
            break;
        }
        case ZYDIS_MNEMONIC_CMPSB: {
            cb.cmpsb();
            break;
        }
        case ZYDIS_MNEMONIC_CMPSW: {
            cb.cmpsw();
            break;
        }
        case ZYDIS_MNEMONIC_MOVSB: {
            cb.movsb();
            break;
        }
        case ZYDIS_MNEMONIC_MOVSW: {
            cb.movsw();
            break;
        }
        case ZYDIS_MNEMONIC_MOVSQ: {
            cb.movsq();
            break;
        }
        case ZYDIS_MNEMONIC_STOSB: {
            cb.stosb();
            break;
        }
        case ZYDIS_MNEMONIC_STOSD: {
            cb.stosd();
            break;
        }
        case ZYDIS_MNEMONIC_STOSW: {
            cb.stosw();
            break;
        }
        case ZYDIS_MNEMONIC_STOSQ: {
            cb.stosq();
            break;
        }
        case ZYDIS_MNEMONIC_SCASB: {
            cb.scasb();
            break;
        }
        case ZYDIS_MNEMONIC_SCASW: {
            cb.scasw();
            break;
        }
        case ZYDIS_MNEMONIC_SCASD: {
            cb.scasd();
            break;
        }
        case ZYDIS_MNEMONIC_SCASQ: {
            cb.scasq();
            break;
        }
        default: {
            uint32_t mnemonic = convertMnemonic(instr.mnemonic);
            cb.emit(mnemonic, ops[0], ops[1], ops[2], ops[3], ops[4]);
            break;
        }
    }

    return true;
}

